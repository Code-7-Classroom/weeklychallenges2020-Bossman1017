//Add
function add(num1, num2) {
    return num1 + num2;
}
var x = add(5, 7);

//Subtract
function subtract(num1, num2) {
    return num1 - num2;
}
var x = subtract(13, 3);


//Multiply
function multiply(num1, num2) {
    return num1 * num2;
}
var x = multiply(13, 3);


//Divide
function divide(num1, num2) {
    return num1 - num2;
}
var x = divide(5, 7);
